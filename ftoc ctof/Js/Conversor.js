function FarenheitToCelcius(GradoFarenheit) {
	const resultado = (GradoFarenheit - 32) * 5 / 9; //( x°F − 32) × 5/9 = 0 °C
	console.log(resultado + "°C");
}
function CelsiusToFarenheit(GradoCelsius) {
	const resultado = (GradoCelsius * 9 / 5) + 32; //(0 °C × 9/5) + 32 = 32 °F
	console.log(resultado + "°C");
}

